public class DataTypes {
    public static void main(String args[]) {
        int int_data = 100; // instance varibale
        double double_data = 1312.123;
        char char_data = 'A';
        float float_data=1.334f;
        long long_data=3123464641252L;
        short short_data=13;
        byte byte_data=1;
        Boolean boolean_data=false;

        System.out.println("int data type : " + int_data);
        System.out.println(" double_data type : " + double_data);
        System.out.println("Char data type : " + char_data);
        System.out.println(" float_data type : " + float_data);
        System.out.println(" long_data type : " + long_data);
        System.out.println(" short_data type : " + short_data);
        System.out.println(" byte_data type : " + byte_data);
        System.out.println(" boolean_data type : " + boolean_data);
    }
}
